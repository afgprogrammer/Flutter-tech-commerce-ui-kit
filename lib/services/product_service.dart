  static List<Product> getFeaturedProducts() {
    return getAllProducts().where((product) => product.isFeatured).toList();
  }

  static List<Product> getProductsByCategory(String categoryId) {
    return getAllProducts().where((product) => product.category == categoryId).toList();
  }

  static List<Product> getOnSaleProducts() {
    return getAllProducts().where((product) => product.isOnSale).toList();
  }

  static Product? getProductById(String id) {
    try {
      return getAllProducts().firstWhere((product) => product.id == id);
    } catch (e) {
      return null;
    }
  }

  static List<Product> searchProducts(String query) {
    final lowercaseQuery = query.toLowerCase();
    return getAllProducts().where((product) {
      return product.name.toLowerCase().contains(lowercaseQuery) ||
             product.description.toLowerCase().contains(lowercaseQuery) ||
             product.brand.toLowerCase().contains(lowercaseQuery) ||
             product.category.toLowerCase().contains(lowercaseQuery);
    }).toList();
  }
